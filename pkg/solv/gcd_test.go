func TestGCD(t *testing.T) {
	var exs []testExample
	_ = json.Unmarshal([]byte(testJSON), &exs)

	for _, e := range exs {
		pred := GCD(e.N1, e.N2)
		if e.GCD != pred {
			t.Errorf("GCD(%d, %d) != %d but should be %d", e.N1, e.N2, pred, e.GCD)
		}
	}
}

